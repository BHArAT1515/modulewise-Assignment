"To compare two lists in Python, you can use the == operator."
"The == operator checks whether the two lists have the same elements in the same order. "
"Here's an example:"

# Example lists
list1 = [1, 2, 3, 4, 5]
list2 = [1, 2, 3, 4, 5]

# Compare lists
if list1 == list2:
    print("The lists are equal.")
else:
    print("The lists are not equal.")

"In this example, the == operator compares each element of list1 with the corresponding element of list2." 
"If all elements are equal and in the same order, the lists are considered equal."